#include<stdio.h>

double SquareMeter(int iValue)
{
    float fSqMtr = 0.0f;
    
    fSqMtr = iValue * 0.0929;

    return fSqMtr;

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Time Complexity : O(1)
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet : \n");
    scanf("%d",&iValue);

    dRet = SquareMeter(iValue);

    printf("%f",dRet);

    return 0;
}