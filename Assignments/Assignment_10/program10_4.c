//
//      Function name : FhtoCs
//      Description :   It is used to to convert temperature from Fahrenheit to Celsius.
//      Input :         Integer
//      Output :        Double
//      Author :        Swayam Satish Gunjal
//      Date :          26/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

double FhtoCs(float fTemp)
{
    float fCel = 1.1f;

    fCel = (fTemp - 32) * (5.0/9.0);

    return fCel;

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Time Complexity : O(1)
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0;

    printf("Enter temperature in Fahrenheit : \n");
    scanf("%f",&fValue);

    dRet = FhtoCs(fValue);

    printf("%f",dRet);

    return 0;
}